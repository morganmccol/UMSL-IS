#include <iostream>
using namespace std;

int main()
{
	double speed, time, distance;

	// Store 20 in the speed variable.
	speed = 172.5;
	cout << speed << endl;
	
	// Store 10 in the time variable.
	time = 27.5;
	cout << time << endl;

	// Multiply speed by time and store the result in the distance variable.
	distance = time / speed;
	
	// Display the contents of the distance variable.
	cout << distance << endl;

	system("PAUSE");
	return 0;
}